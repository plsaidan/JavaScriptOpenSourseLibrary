//while loop loops through a block of code as long as a specified condition is true.
// loops are used to repeatedly run a block of code
var sum = 0;
var number = 1;
while (number <= 50) {  // condition
  sum += number;        // body
  number++;             // updater
}
alert("Sum = " + sum);  // => Sum = 1275

//An alert box should pop up giving me the answer to my problem, 1275
